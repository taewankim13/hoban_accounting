#!/bin/bash
# ============================================================
#  호반건설 전표 이상탐지 시스템 (FDS) — 배포 스크립트
#  실행: bash deploy.sh
# ============================================================
set -e

echo "=========================================="
echo " 호반건설 FDS 배포 시작"
echo "=========================================="

# -----------------------------------------------------------
# 1) .env 파일 확인
# -----------------------------------------------------------
if [ ! -f .env ]; then
    echo ""
    echo "[설정] .env 파일이 없습니다. 생성합니다."
    read -p "LLM API Key를 입력하세요: " api_key
    echo "ALPHA_API_KEY=${api_key}" > .env
    echo "[설정] .env 파일 생성 완료"
else
    echo "[설정] .env 파일 확인 완료"
fi

# -----------------------------------------------------------
# 2) Docker 빌드 & 실행
# -----------------------------------------------------------
echo ""
echo "[빌드] Docker 이미지 빌드 중..."
docker compose build

echo ""
echo "[실행] 컨테이너 시작 중..."
docker compose up -d

echo ""
echo "=========================================="
echo " 배포 완료!"
echo ""
echo " 접속 주소: http://$(hostname -I | awk '{print $1}'):8001"
echo " 로그 확인: docker compose logs -f"
echo " 중지:      docker compose down"
echo " 재시작:    docker compose restart"
echo "=========================================="
